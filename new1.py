# 百钱百鸡问题:公鸡5元一只，母鸡3元一只，小鸡一元3只。 如何100元买100只鸡？

# 设公鸡x只，母鸡y只，小鸡z只



for x in range (101):
    for y in range (101):
        for z in range (101):
            if x+y+z ==100:
                if 5*x+3*y+1/3*z ==100:
                    print (str(x)+" "+str(y)+" "+str(z))
